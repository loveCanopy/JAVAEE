package com.model;

public class Dessert {
	private int DessertID;
	private String DessertName;
	private String DessertDescription;
	private int DessertSellingPrice;
	private String DessertPicture;
	private int DessertStorage;
	
	public int getDessertID() {
		return DessertID;
	}
	public void setDessertID(int dessertID) {
		DessertID = dessertID;
	}
	public String getDessertName() {
		return DessertName;
	}
	public void setDessertName(String dessertName) {
		DessertName = dessertName;
	}
	public String getDessertDescription() {
		return DessertDescription;
	}
	public void setDessertDescription(String dessertDescription) {
		DessertDescription = dessertDescription;
	}
	public int getDessertSellingPrice() {
		return DessertSellingPrice;
	}
	public void setDessertSellingPrice(int dessertSellingPrice) {
		DessertSellingPrice = dessertSellingPrice;
	}
	public String getDessertPicture() {
		return DessertPicture;
	}
	public void setDessertPicture(String dessertPicture) {
		DessertPicture = dessertPicture;
	}
	public int getDessertStorage() {
		return DessertStorage;
	}
	public void setDessertStorage(int dessertStorage) {
		DessertStorage = dessertStorage;
	}

}
